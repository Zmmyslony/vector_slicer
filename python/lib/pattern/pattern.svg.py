from svgpathtools import svg2paths, wsvg, path
import cmath
import numpy as np

def bbox_list(paths_list):
    x_min: float = 1e308
    y_min: float = 1e308
    x_max: float = -1e308
    y_max: float = -1e308

    for path in paths_list:
        x_min_p, x_max_p, y_min_p, y_max_p = path.bbox()
        if x_min_p < x_min: x_min = x_min_p
        if y_min_p < y_min: y_min = y_min_p
        if x_max_p > x_max: x_max = x_max_p
        if y_max_p > y_max: y_max = y_max_p
    return x_min, x_max, y_min, y_max


def angle(path, normalised_length):
    point_c = path.point(normalised_length)
    point = np.array([point_c.real, point_c.imag])
    tangent = path.unit_tangent(normalised_length)
    angle = np.arctan2(tangent.imag, tangent.real)
    return point, np.remainder(angle, np.pi)


